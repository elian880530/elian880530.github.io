    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        int posv1 = 0;
        int posv2 = 0;
        String a = jComboBox1.getSelectedItem().toString();
        String b = jComboBox2.getSelectedItem().toString();
        try 
        {
            if(jComboBox1.getSelectedItem().equals("Seleccione") || jComboBox2.getSelectedItem().equals("Seleccione"))
            {
                JOptionPane.showMessageDialog(jFrame1,"Debe seleccionar el nombre de las empresas","Error",JOptionPane.ERROR_MESSAGE);
            }
            else if(jTextField4.getText().equals(""))
            {
                JOptionPane.showMessageDialog(jFrame1,"Debe entrar el costo de conexi?n","Error",JOptionPane.ERROR_MESSAGE);
            }
            else if(jComboBox1.getSelectedItem().equals(jComboBox2.getSelectedItem()))
            {
                JOptionPane.showMessageDialog(jFrame1,"No se puede conectar una misma empresa","Error",JOptionPane.ERROR_MESSAGE);
            }
            else if(g.EstaLaArista(jComboBox1.getSelectedItem().toString(), jComboBox2.getSelectedItem().toString()))
            {
                JOptionPane.showMessageDialog(jFrame1,"Ya existe la coneccion","Error",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                for(int i = 1; i <= g.getVertices().Longitud(); i++) 
                {
                    if(a == g.getVertices().Obtener(i).getDato())
                    {
                        posv1 = i;
                        if(posv2 != 0)
                            break;
                    }
                    else if(b == g.getVertices().Obtener(i).getDato())
                    {
                        posv2 = i;
                        if(posv1 != 0)
                            break;
                    }
                }
                g.AdicionarArista(g.getVertices().Obtener(posv1), g.getVertices().Obtener(posv2), Integer.parseInt(jTextField4.getText()));
                jTextField4.setText("");
                jComboBox1.setSelectedItem("Seleccione");
                jComboBox2.setSelectedItem("Seleccione");
                DefaultListModel listmodel2 = new DefaultListModel();
                for(int i = 1; i <= g.getAristas().Longitud(); i++) 
                {
                    listmodel2.addElement(String.valueOf(g.getAristas().Obtener(i).getPeso()));
                }
                jButton3.setEnabled(true);
                ActualizarTabla(jTable1);
            }
        } 
        catch (Exception e) 
        {
            e.getMessage();
        }
    }//GEN-LAST:event_jButton2MouseClicked


    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try 
        {
            if(jTextField1.getText().equals(""))
            {
                JOptionPane.showMessageDialog(jFrame1,"Debe entrar el nombre de la empresa","Error",JOptionPane.ERROR_MESSAGE);
            }
            else if(g.EstaElVertice(jTextField1.getText()))
            {
                JOptionPane.showMessageDialog(jFrame1,"No se puede adicionar una empresa existente","Error",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                CListaSE<String> provincias = new CListaSE<String>();
                if(g.getVertices().Longitud() == 0)
                {
                    g.AdicionarVertice(jTextField1.getText().toString());
                    provincias.Adicionar(jTextField1.getText().toString());
                }
                else
                {
                    g.AdicionarVertice(jTextField1.getText().toString());
                    provincias.Adicionar(jTextField1.getText().toString());
                    
                    
                    DefaultListModel listmodel2 = new DefaultListModel();
                    for(int i = 1; i <= g.getAristas().Longitud(); i++) 
                    {
                        listmodel2.addElement(String.valueOf(g.getAristas().Obtener(i).getPeso()));
                    }
                    jButton3.setEnabled(true);
                    ActualizarTabla(jTable1);
                
                }
                jTextField1.setText("");
                DefaultListModel listmodel = new DefaultListModel();
                DefaultComboBoxModel comboboxmodel = new DefaultComboBoxModel();
                DefaultComboBoxModel comboboxmodel1 = new DefaultComboBoxModel();
                for(int i = 1; i <= g.getVertices().Longitud(); i++) 
                {
                    listmodel.addElement(g.getVertices().Obtener(i).getDato());
                    comboboxmodel.addElement(g.getVertices().Obtener(i).getDato());
                    comboboxmodel1.addElement(g.getVertices().Obtener(i).getDato());
                }
                jComboBox1.setModel(comboboxmodel);
                jComboBox2.setModel(comboboxmodel1);
                jList1.setModel(listmodel);
                comboboxmodel.insertElementAt("Seleccione",0);
                comboboxmodel1.insertElementAt("Seleccione",0);
                comboboxmodel.setSelectedItem("Seleccione");
                comboboxmodel1.setSelectedItem("Seleccione");
                jMenuItem3.setEnabled(true);
                jTextField1.requestFocus();
            }
        } 
        catch (Exception e) 
        {
            e.getMessage();
        }
    }//GEN-LAST:event_jButton1MouseClicked

