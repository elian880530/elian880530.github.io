    public void ActualizarTabla(JTable tabla)
    {
        try
        {
            CListaSE<CArista> aristas = g.getAristas();
            Object [][] arreglo = new Object[aristas.Longitud()][3];
            for(int i = 0; i < aristas.Longitud(); i++)             
            {
                arreglo[i][0] = aristas.Obtener(i + 1).getV1().getDato();
                arreglo[i][1] = aristas.Obtener(i + 1).getV2().getDato();
                arreglo[i][2] = aristas.Obtener(i + 1).getPeso();
            }
            tabla.setModel(new javax.swing.table.DefaultTableModel(arreglo,new String [] {"Nombre", "Nombre", "Costo"}));        
        }
        catch (Exception e)
        {
            e.getMessage();
        }
    }
