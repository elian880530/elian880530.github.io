
public class Persona {
	int Solapin;
	String Nombre;
	
	

	public Persona(int solapin, String nombre) {
		super();
		Solapin = solapin;
		Nombre = nombre;
	}

	public String getNombre() {
		return Nombre;
	}

	public void setNombre(String nombre) {
		Nombre = nombre;
	}

	public int getSolapin() {
		return Solapin;
	}

	public void setSolapin(int solapin) {
		Solapin = solapin;
	}

	
	
	
}
